class Railway{
	static String stationName;
	static String stationId;
	static String stationManager;
	static long stationContactNo; 
	static String trainShedule;
	
	
	static void setStationName(String nam){
		stationName=nam;
	}
	static String getStationName(){
		return stationName;
	}
		static void setStationId(String Id){
		stationId=Id;
	}
	static String getStationId(){
		return stationId;
	}
		static void setStationManager(String man){
		stationManager=man;
	}
	static String getStationManager(){
		return stationManager;
	}
	static void setStationContactNo(long no){
		stationContactNo=no;
	}
	static long getStationContactNo(){
		return stationContactNo;
	}
		static void setTrainShedule(String sed){
		trainShedule=sed;
	}
	static String getTrainShedule(){
		return trainShedule;
	}

	
}